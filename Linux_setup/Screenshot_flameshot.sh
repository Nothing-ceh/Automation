# Install  flameshot tool for screenshot.
# Ubuntu based distribution
wget https://github.com/flameshot-org/flameshot/releases/download/v0.9.0/flameshot-0.9.0-1.ubuntu-20.04.amd64.deb
# Executebale permission
 chmod u+x flameshot-0.9.0-1.ubuntu-20.04.amd64.deb
 # Install 
 sudo dpkg -i flameshot-0.9.0-1.ubuntu-20.04.amd64.deb
 # remove flameshot download file 
 rm -f flameshot-0.9.0-1.ubuntu-20.04.amd64.deb
